        created_count = 0
        for post_data in blog_posts:
            slug = slugify(post_data["title"])
            # Проверяем, существует ли уже статья с таким slug
            if Blog.objects.filter(slug=slug).exists():
                # Добавляем уникальный суффикс
                counter = 1
                while Blog.objects.filter(slug=f"{slug}-{counter}").exists():
                    counter += 1
                slug = f"{slug}-{counter}"

            blog, created = Blog.objects.get_or_create(
                slug=slug,
                defaults={
                    "title": post_data["title"],
                    "author": author,
                    "content": post_data["content"],
                    "category": category,
                    "status": "Published",
                    "date": timezone.now(),
                }
            )

            if created:
                created_count += 1
                self.stdout.write(
                    self.style.SUCCESS(f'Создана статья: "{post_data["title"]}"')
                )
            else:
                self.stdout.write(
                    self.style.WARNING(f'Статья уже существует: "{post_data["title"]}"')
                )

        self.stdout.write(
            self.style.SUCCESS(
                f'\nУспешно создано {created_count} новых статей о бытовой технике!'
            )
        )

